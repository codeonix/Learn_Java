public class Comparison {
    public static void main (String[] args)
{
    System.out.println("Comparison Operators:");
    int age = 18;
    if (age==18){
        System.out.println("You are 18 years old");
    }
    else if (age >18){
        System.out.println("You are eligible to vote");
    }
    else{
        System.out.println("You are not eligible to vote");
    }

    // == (Equal to )
    int a = 27;
    if (a ==27){
        System.out.println("You are 27 years old");

    }

    // <(Less Than)
    if (a <30){
        System.out.println("a is less than 30");
    }

    // > (Greater than)
    if (a>18){
        System.out.println(" a is greater than 18");
    }

    //<= (Less than or equal to)
    if (a<=30){
        System.out.println("a is less than or equal to 30");

    }
    //>= (Greater than or equal to )
    if (a>=18){
        System.out.println("A is greater than or equal to 18");
    }

    
}
    
}
